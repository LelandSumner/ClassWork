import java.util.*;

public class PrintRange{

    public static void main (String [] args) {
	System.out.println("printRange(2,7) should be 2 3 4 5 6 7 and you get");
	printRange(2,7);
	System.out.println("printRange(19,11) should be 19 18 17 16 15 14 13 12 11 and you get");
	printRange(19,11);
	System.out.println("printRange(5,5) should be 5 and you get");
	printRange(5,5);
	System.out.println("printRange(-5, 2) should be -5 -4 -3 -2 -1 0 1 2 and you get");
	printRange(-5, 2);
	System.out.println("printRange(1,0) should be 1 0  and you get");
	printRange(1,0);
	System.out.println("printRange(-6.-9) should be -6 -7 -8 -9  and you get");
	printRange(-6, -9);
    }

    // put printRange() here

}	
