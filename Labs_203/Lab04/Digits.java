public class Digits {
  public static final int n=8
  ;
    public static void main (String [] args) {
        printDigits();
        System.out.println();
    }
    
    public static void printDigits() {
      
      for (int m=1; m<=n; m++) {
        for (int i=0; i<=9; i++){
	  for(int j=1; j<=n; j++){
	  System.out.print(i);
          }
       }
      System.out.println();
      }
    }    

}
