public class Sequences1 {

    public static void main (String [] args) {

        System.out.println("Printing 4, 19, 34, 49, 64, 79, 94, 109, 124, 139");
        // Print 4, 19, 34, 49, 64, 79, 94, 109, 124, 139 again
        //  by changing the loop header ONLY
        for (int i = 1; i <= 10; i++) {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();
    
        System.out.println("Printing 30, 20, 10, 0, -10, -20, -30, -40, -50, -60");
        // Print 30, 20, 10, 0, -10, -20, -30, -40, -50, -60 again
        //  by changing the loop header ONLY
        for (int i = 1; i <= 10; i++) {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();
    
        System.out.println("Printing -7, -3, 1, 5, 9, 13, 17, 21, 25, 29");
        // Print -7, -3, 1, 5, 9, 13, 17, 21, 25, 29 again
        //  by changing the loop header ONLY
        for (int i = 1; i <= 10; i++) {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();
    
        System.out.println("Printing 97, 94, 91, 88, 85, 82, 79, 76, 73, 70");
        // Print 97, 94, 91, 88, 85, 82, 79, 76, 73, 70 again
        //  by changing the loop header ONLY
        for (int i = 1; i <= 10; i++) {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();
        
    } // end main

} // end class
